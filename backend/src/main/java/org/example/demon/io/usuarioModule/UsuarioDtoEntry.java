package org.example.demon.io.usuarioModule;

public class UsuarioDto {
    private String nome;
    private String email;

    public usuarioDto() {
    }

    public String getNome() {
        return nome;
    }

    public void setNome(String nome) {
        this.nome = nome;
    }

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

}
